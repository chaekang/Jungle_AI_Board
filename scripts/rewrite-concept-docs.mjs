function getConcept(name) {
  const key = aliases[name] ?? name;
  const concept = concepts[key];

  if (!concept) {
    throw new Error(`Missing concept metadata: ${name}`);
  }

  return concept;
}

function stripFinalPunctuation(value) {
  return value.replace(/[.!?。！？]+$/u, "");
}

function pickPathLabel(pathDescription) {
  return pathDescription.match(/`([^`]+)`/)?.[1] ?? pathDescription;
}

function formatInlineConceptList(items) {
  return items.map((item) => `\`${item}\``).join(", ");
}

